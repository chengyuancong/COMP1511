// A simple program demonstrating the mod % operator. 
// % gives the remainder


#include <stdio.h>

int main(void){     
   printf("10 mod 2 is %d\n",10 % 2);       
   printf("7 mod 2 is %d\n",7%2);           
   printf("2 mod 2 is %d\n",2%2);           
   printf("9876 mod 10 is %d\n",9876%10);   
   printf("9876 mod 100 is %d\n",9876%100);
   printf("9876/10 is %d\n",9876/10);       
   return 0;
}